                <img src="piala.jpeg"class="featured-image"alt="juara">
                <p>Sepanjang berdirinya klub, Liverpool telah mengoleksi 18 tropi Liga Utama Inggris. Selama 16 tahun Premiere League bergulir, Liverpool belum pernah memenangkan title tersebut sekalipun. Liverpool memegang rekor 7 trofi juara Piala Liga, selisih 2 gelar dengan Aston Villa. Liverpool pernah meraih gelar ganda dengan menjuarai Liga dan Piala FA pada tahun 1986. Mereka juga pernah memenangkan tiga trofi dalam satu musim (treble winner) sebanyak 2 kali, yang pertama mereka memenangkan Liga Inggris, Piala Liga dan Piala Champion pada tahun 1984, dan yang kedua pada tahun 2001 dengan meraih Piala FA, Piala Liga dan Piala UEFA. Liverpool juga pernah meraih gelar ganda eropa dengan menjuarai Liga dan Piala Champion eropa pada tahun 1977.</p>

