                <h2 id="sejarah">Sejarah</h2>
                 <img src="download.jfif" class="featured-image" alt="sejarah">
                <p>Sejarah Klub Sepak Bola Liverpool dibagi menjadi tiga periode: Sejarah Liverpool F.C. - Klub ini didirikan pada tahun 1892 setelah berpisah dari Everton F.C. dan bergabung dengan Football League pada tahun 1893</p>

                <h3>Masa awal</h3>

                <p>Kembali ke sejarah Liverpool Fc pada masa awal, klub ini didirikan pada tanggal 15 maret 1892. Klub ini didirikan setelah perseteruan antara komite Everton Fc dengan John Houlding yaitu presiden klub sekaligus pemilik stadion Anfield. Pertengkaran terjadi setelah John Houlding membeli Stadion Anfield dan menaikan harga sewa stadion kepada Everton Fc dari harga £ 100 sampai £ 250 per tahun.

                 </p>

                <p>Everton, yang telah bermain di Anfield selama tujuh tahun, menolaknya dan terjadi perseteruan. Setelah Everton memutuskan untuk tidak melanjutkan menyewa Anfield, dan Houlding akan membuat klub sendiri yaitu Liverpool FC.</p>

                <h3>Masa perkembangan</h3>

                <p>Sejarah Liverpool Fc berikutnya adalah pada masa perkembangan. Liverpool pertama kali mencapai final piala FA pertama pada tahun 1914, walaupun pada akhirnya dikalahkan Burnley 1-0. Selanjutnya, Liverpool meraih juara liga 2 musim yaitu pada tahun 1921-1922 dan musim 1922-1923. Nah, selanjutnya Liverpool Fc memasuki masa yang kurang beruntung yaitu tidak mendapatkan trofi hingga musim 1946-1947 ketika berhasil meraih gelar liga yang ke 5.</p>

                <p>Setelah berada pada divisi satu selama lebih dari 50 tahun, akhirnya Liverpool Fc jatuh dan mengalami penurunan kualitas ke divisi II pada musim 1953-54. Pada piala FA musim 1958-59, Bill Shankly ditunjuk sebagai manjer setelah Liverpool dikalahkan oleh Worcester City. Shankly melakukan perombakan besar-besaran dengan melepas 24 pemain lama dan menggunakan sebuah ruangan di Stadion Anfield untuk menggelar rapat pelatihan. Ruangan tersebut dinamakan “The Boot Room” yang sudah berhasil melahirkan manajer-manajer legendaris Liverpool di masa mendatang.</p>

                <h3>Masa kejayaan</h3>
                <img src="download_1.jfif" class="image"alt="kejayaan">
                <p>Sejarah Liverpool Fc selanjutnya memasuki masa kejayaannya. Liverpool menggapai era terbaiknya di bawah kepemimpinan manajer Bill Shankly. Pelatih tersebut kemudian menjadi legenda Liverpool. Pelatih tersebut dihormati karena berhasil membawa Liverpool kembali menjadi ke divisi satu setelah berada pada divisi dua selama 8 musim. Untuk menghormati jasanya tersebut, klub Liverpool membuat patung Bill Shankly di pintu masuk Anfield.</p>
