                <h2 id="Stadion">Stadion</h2>
                <img src="download_2.jpg" class="featured-image" alt="Stadion Anfield">
                <p>KAnfield adalah nama stadion yang terletak di kota Liverpool Inggris, yang memiliki kapasitas tempat duduk sebanyak 54,074, membuat stadion ini menjadi stadion terbesar ketujuh di Inggris. Stadion ini merupakan kandang bagi klub sepak bola Liverpool F.C. sejak klub didirikan pada tahun 1892. Stadion ini awalnya merupakan kandang dari klub Everton F.C. dari tahun 1884 hingga 1892 sebelum Everton pindah ke stadion Goodison Park setelah berselisih dengan pemilik klub.</p>

                <p>Stadion ini memiliki empat tribun: , Tribun Utama, Tribun Sir Kenny Dalglish dan Anfield Road End. Rekor kehadiran penonton sebanyak 61,905 diraih saat pertandingan antara Liverpool melawan Wolverhampton Wanderers di tahun 1952.Stadion ini berubah menjadi stadion tempat duduk di tahun 1994 karena hasil Laporan Taylor, yang sekaligus mengurangi kapasitas stadion.</p>

                <p>Dua gerbang di stadion dinamai mantan pelatih Liverpool: Bill Shankly dan Bob Paisley. Sebuah patung Shankly juga diletakkan di luar stadion. Stadion ini berjarak 2 mil (3 km) dari Stasiun Kereta Liverpool Lime Street. Stadion ini diusulkan di tahun 2002 untuk diganti dengan yang baru yang lokasinya berdekatan dengan Stanley Park, namun setelah akuisisi Liverpool F.C. oleh Fenway Sports Group menjadi jelas bahwa hal tersebut tidak akan terjadi.</p>

                <p>Pembangunan untuk perluasan Tribun utama dimulai pada 8 Desember 2014. Perluasan tersebut meningkatkan kapasitas stadion menjadi 54,074, membuat Anfield menjadi salah satu stadiun bertempat duduk terbesar di persepakbolaan Eropa. Terdapat rencana untuk memperluas Tribun Anfield Road End yang akan membuat kapasitas stadion menjadi 61,000. Liverpool F.C. secara resmi membuka Tribun Utama yang selesai direnovasi pada 9 September 2016.</p>
